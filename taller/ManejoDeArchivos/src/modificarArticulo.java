    /**
     * crea la ventana y llena los campos de texto con los valores del articulo
     * @param refAlm
     * @param codigo 
     */
    public modificarArticulo(InterfaceDB refAlm, int codigo) {
        initComponents();
        this.refAlm = refAlm;
        codAnterior=codigo;
        articulo art = refAlm.buscarArticuloPorCodigo(codigo);
        if (art!=null){
            txtCodigo.setText(Integer.toString(art.codigo));
            txtCantidad.setText(Integer.toString(art.cantidad));
            txtValor.setText(Integer.toString(art.valor));
            txtNombre.setText(art.nombre);
        }else{
            JOptionPane.showMessageDialog(rootPane,"El Articulo no Existe");
            dispose();
        }
        
    }

    
    

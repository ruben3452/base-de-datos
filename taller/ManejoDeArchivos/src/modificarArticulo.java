    /**
    * comprueba que el codigo,cantidad y valor sean numeros y luego lo intenta modificar 
    * @param evt 
    */
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codigo = Integer.valueOf(txtCodigo.getText());
        if (refAlm.buscarArticuloPorCodigo(codigo)!=null && codAnterior!=codigo){
            JOptionPane.showMessageDialog(rootPane, "Ya existe un articulo con este codigo");
            return;
        }
        
        articulo art = new articulo(codigo, txtNombre.getText(), Integer.valueOf(txtCantidad.getText()), Integer.valueOf(txtValor.getText()));
        if (refAlm.modificarArticulo(codAnterior,art)){
            JOptionPane.showMessageDialog(rootPane, "El articulo fue modificado con exito");
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error ingresando el articulo");
       }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    
    
    
